Russian Chat command translation file
---------------------------------------------------------------------------

Этот файл содержит перевод описаний команд чата.
Слева находятся команды, а справа — их описание.
При переводе этих описаний убедитесь, что все двойные кавычки (" ") и запятые после них остались на своих местах!

= Внимание =
Иногда при обновлении DarkRP добавляются новые команды, а старые обновляются или удаляются.
По умолчанию непереведённые команды используют английское описание.
Переводы команд чата, которых не существует, игнорируются.


= Перевод отсутствующих описаний команд =
1. Запустите сервер;
2. Подключитесь к нему;
3. В консоли введите следующую команду:
    darkrp_translateChatCommands
4. Вернитесь в этот файл;
5. Вставьте отсутствующие строки в раздел "-- Добавляйте новые строки после последней строки в этом разделе:"

= Примечание =
Убедитесь в том, что языковой код находится в самом конце файла.

= Использование перевода =
Убедитесь в том, что консольная переменная gmod_language установлена на русский язык (ru). Вы можете настроить её в CFG-файле сервера.

